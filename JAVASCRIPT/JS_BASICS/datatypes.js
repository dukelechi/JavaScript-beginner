console.log(x);
console.log(typeof x);

// string
var x = "Hello World";
console.log(x);
console.log(typeof x);

// boolean
var x = true;
console.log(x);
console.log(typeof x);

//symbol
var  x = Symbol(100);
console.log(x);
console.log(typeof x);

//undefined
var x = undefined;
console.log(x);
console.log(typeof x);

//null
var x = null;
console.log(x);
console.log(typeof x)


